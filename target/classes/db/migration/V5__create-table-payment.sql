CREATE TABLE TB_PAYMENT(
	ID_PAYMENT SERIAL PRIMARY KEY,
	DS_PAYMENT VARCHAR(100) NOT NULL,
	ID_TYPE_PAYMENT INT NOT NULL,
	ID_STATEMENT INT NOT NULL, 
	ID_BANK INT NOT NULL, 
	FOREIGN KEY (ID_TYPE_PAYMENT) REFERENCES TB_TYPE_PAYMENT(ID_TYPE_PAYMENT), 
	FOREIGN KEY (ID_STATEMENT) REFERENCES TB_STATEMENT(ID_STATEMENT), 
	FOREIGN KEY (ID_BANK) REFERENCES TB_BANK(ID_BANK)
);